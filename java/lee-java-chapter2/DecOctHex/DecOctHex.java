import java.util.Scanner;
public class DecOctHex {
	public static void main(String argv[]) {
/* 	10、8、16 進位的數值表示
*	10 進位 : 100
*	8 進位 : 0100
*	16 進位 : 0x100
*/
		System.out.println("10 進位 (100) : " + 100 + "\n");
		System.out.println("8 進位 (0100) : " + 0100 + "\n");
		System.out.println("16 進位 (0x100) : " + 0x100 + "\n");
	}
}