import java.util.Scanner;
public class LongConst {
	public static void main(String argv[]) {
/* 	整數
*	正常整數不論是正值還是負值都沒有小數點
*	整數後加上 大寫 "L"代表長整數
*	EX : 10000000000L
*/
		System.out.print("第一個值  : " + 10000 + "\n");
		System.out.print("第二個值 : " + 1000000000 + "\n");
		System.out.print("第三個值 (有加L) : " + 10000000000L + "\n");
	}
}